class McpAdapter:
    """MCP adapter for Code Mode execute_fn."""

    def __init__(
        self,
        server_script: str | None = None,
        tools_registry: list[dict] | None = None,
    ):
        self.server_script = server_script
        self.tools_registry = tools_registry or load_tools_registry(server_script)
        self._tools_by_name = {t["name"]: t for t in self.tools_registry}

    def get_tool(self, name: str) -> dict | None:
        """Look up a tool by name."""
        return self._tools_by_name.get(name)

    def list_tools(self) -> list[dict]:
        """Return all available tools."""
        return self.tools_registry

    def execute(self, normalized_request: dict) -> tuple[bool, Any]:
        """Execute a normalized MCP tool call. Returns (ok, result).

        This is the execute_fn passed to run_codemode().
        """
        tool_name = normalized_request["tool"]
        args = normalized_request.get("args", {})

        tool = self.get_tool(tool_name)
        if tool is None:
            return False, {"error": f"Tool '{tool_name}' not found in registry"}

        return invoke_tool(tool_name, args, self.server_script)
