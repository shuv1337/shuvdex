def invoke_tool(tool_name: str, args: dict, server_script: str | None = None) -> tuple[bool, Any]:
    """Invoke an MCP tool by name with the given arguments.

    Override this to match your MCP server's invocation mechanism.

    Returns (ok: bool, result: Any).
    For sidecar mode, this typically calls the MCP server via mcporter or subprocess.
    """
    if server_script is None:
        server_script = str(Path(__file__).parent / "make_api_server.py")

    # Default implementation: use mcporter CLI
    try:
        result = subprocess.run(
            [
                sys.executable, "-m", "mcporter",
                "call", f"{{make-api}}.{tool_name}",
                "--args", json.dumps(args),
            ],
            capture_output=True,
            text=True,
            timeout=30,
        )

        if result.returncode == 0:
            try:
                parsed = json.loads(result.stdout)
                return True, parsed
            except json.JSONDecodeError:
                return True, {"raw": result.stdout.strip()}
        else:
            return False, {"error": result.stderr.strip() or "Tool invocation failed"}

    except subprocess.TimeoutExpired:
        return False, {"error": f"Tool '{tool_name}' timed out"}
    except Exception as exc:
        return False, {"error": str(exc)}


