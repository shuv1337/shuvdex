"""
Code Mode Host Broker — orchestrates sandboxed execution via Node runner subprocess.

The host broker:
  1. Spawns the Node runner with an init payload
  2. Mediates all RPC requests (REST API calls or MCP tool calls)
  3. Enforces policy gates on every mediated operation
  4. Collects audit records and console output
  5. Returns a standard JSON envelope

Generated by codemode-scaffold. Template version: 1.0.0
"""

