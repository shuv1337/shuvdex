# --- Error taxonomy ---

class CodemodeError(Exception):
    """Base error for all Code Mode failures."""
    error_type = "RuntimeError"

    def __init__(self, message: str, details: dict | None = None):
        super().__init__(message)
        self.message = message
        self.details = details or {}

    def to_payload(self) -> dict:
        return {
            "type": self.error_type,
            "message": self.message,
            "details": self.details,
        }


class ValidationError(CodemodeError):
    """Input validation failure (bad code, missing args, malformed request)."""
    error_type = "ValidationError"


class PolicyError(CodemodeError):
    """Policy violation (write without permission, method not allowed, cap exceeded)."""
    error_type = "PolicyError"


class RuntimeExecutionError(CodemodeError):
    """Runtime failure (runner crash, node not found, unexpected error)."""
    error_type = "RuntimeError"


